#include <iostream>
#include <math.h>
using namespace std;

main(){
    cout << "ceil(5.62)= " << ceil(5.62) << endl;
    cout << "floor(4.32)= " << floor(4.32) << endl;
    cout << "ceil(-7.28)= " << ceil(-7.28) << endl;
    cout << "floor(-4.33)= " << floor(-4.33) << endl;
    cout << "fabs(-120)= " << fabs(-120) << endl;
    cout << "sqrt(16)= " << sqrt(16) << endl;
    cout << "pow(2,3)= " << pow(2,3) << endl;
}
